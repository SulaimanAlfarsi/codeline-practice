/**
 * StarMatrix - Prints a 5x5 grid of asterisks.
 * 
 * This class demonstrates basic output by printing a grid pattern
 * where each asterisk is separated by a single space.
 */
public class StarMatrix {
    /**
     * Main method that prints a 5x5 star grid.
     * Each row contains 5 asterisks separated by spaces.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        System.out.println("* * * * *");
        System.out.println("* * * * *");
        System.out.println("* * * * *");
        System.out.println("* * * * *");
        System.out.println("* * * * *");
    }
}