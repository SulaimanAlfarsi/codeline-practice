/**
 * ProductPro - Demonstrates multiplication of double values.
 * 
 * This class shows how to declare double variables and calculate
 * their product using the multiplication operator (*).
 */
public class ProductPro {
    /**
     * Main method that calculates the product of two doubles.
     * Declares num1 (12.5) and num2 (4.0), then calculates their product.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        double num1;
        double num2;
        double product;
        num1 = 12.5;
        num2 = 4.0;
        product = num1 * num2;
        System.out.println(product);
    }
}