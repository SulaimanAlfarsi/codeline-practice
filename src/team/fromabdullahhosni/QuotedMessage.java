/**
 * QuotedMessage - Prints a quoted message to the console.
 * 
 * This class demonstrates using the escape character (\") to include
 * quotation marks within a string that is printed to the console.
 */
public class QuotedMessage {
    /**
     * Main method that prints a quoted message.
     * Uses escape sequence \" to display quotes around the phrase.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        System.out.println("He said, \"Practice makes perfect!\"");
    }
}