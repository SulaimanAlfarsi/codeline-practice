/**
 * AsteriskFrame - Prints a hollow box made of asterisks.
 * 
 * This class creates a 5x4 hollow rectangle using asterisks,
 * with only the border shown (interior is empty).
 */
public class AsteriskFrame {
    /**
     * Main method that prints a 5x4 hollow asterisk box.
     * Top and bottom rows are filled with asterisks.
     * Middle rows have asterisks only at the edges.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        System.out.println("*****");
        System.out.println("* *");
        System.out.println("* *");
        System.out.println("*****");
    }
}