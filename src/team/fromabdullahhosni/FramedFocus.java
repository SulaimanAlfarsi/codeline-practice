/**
 * FramedFocus - Displays a message inside a star frame.
 * 
 * This class prints a rectangular frame made of asterisks (*)
 * with the message "Keep Learning" centered inside.
 */
public class FramedFocus {
    /**
     * Main method that prints a framed message.
     * Uses escape character \" to include quotes in output.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        System.out.println("***********************");
        System.out.println("* *");
        System.out.println("* \"Keep Learning\"   *");
        System.out.println("* *");
        System.out.println("***********************");
    }
}