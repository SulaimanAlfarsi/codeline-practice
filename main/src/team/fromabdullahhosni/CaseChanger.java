package team.fromabdullahhosni;

/**
 * CaseChanger - Demonstrates converting strings to upper and lower case.
 * 
 * This class shows how to use toUpperCase() and toLowerCase() methods
 * to transform the case of characters in a string.
 */
public class CaseChanger {
    /**
     * Main method that converts string to different cases.
     * Declares a String and prints it in both uppercase and lowercase.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        String text;
        text = "Sprint One";
        System.out.println(text.toUpperCase());
        System.out.println(text.toLowerCase());
    }
}