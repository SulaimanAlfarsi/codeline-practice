package team.fromabdullahhosni.sprint1;

/**
 * StringSecret - Demonstrates finding the length of a string.
 * 
 * This class shows how to use the length() method to determine
 * the number of characters in a String value.
 */
public class StringSecret {
    /**
     * Main method that calculates and prints string length.
     * Declares a String variable and uses the length() method
     * to find and display the number of characters.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        String text;
        text = "Java Programming";
        System.out.println(text.length());
    }
}
