package team.fromabdullahhosni.sprint1;

/**
 * QuotedDialogues - Displays a conversation with quotation marks.
 * 
 * This class demonstrates the use of escape character (\") to include
 * quotation marks in the output, showing a dialogue between teacher and student.
 */
public class QuotedDialogues {
    /**
     * Main method that prints a conversation.
     * Uses escape sequence \" to display quotes in console output.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        System.out.println("Teacher: \"What is your name?\"");
        System.out.println("Student: \"My name is Alex.\"");
        System.out.println("Teacher: \"Welcome, Alex!\"");
    }
}
