package team.fromabdullahhosni.sprint1;

/**
 * SimpleSum - Demonstrates basic integer addition.
 * 
 * This class shows how to declare integer variables and perform
 * simple arithmetic using the addition operator (+).
 */
public class SimpleSum {
    /**
     * Main method that calculates the sum of two integers.
     * Declares num1 (15) and num2 (25), then calculates their sum.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        int num1;
        int num2;
        int sum;
        num1 = 15;
        num2 = 25;
        sum = num1 + num2;
        System.out.println(sum);
    }
}
