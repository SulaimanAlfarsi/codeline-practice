package team.fromabdullahhosni;

/**
 * StaircaseChallenge - Prints a staircase pattern using # symbols.
 * 
 * This class demonstrates basic output printing by creating a visual
 * staircase pattern with 5 steps using the hash symbol.
 */
public class StaircaseChallenge {
    /**
     * Main method that prints a 5-step staircase pattern.
     * Each line increases the number of # symbols by one.
     * 
     * @param args command line arguments (not used)
     */
    public static void main(String[] args) {
        System.out.println("#");
        System.out.println("##");
        System.out.println("###");
        System.out.println("####");
        System.out.println("#####");
    }
}